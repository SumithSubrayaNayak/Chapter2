#COMPARISON OPERATOR
#they are also called relational operators

#equal to     ==
#not equal to !=
#less than     <
#greater than  >
#less than or equal to <=
#greater than or equal to >=

#it returns boolean value as output
#equal to
print("hello"=="hello")     #returns true
print("hello"=="Hello")     #returns false

#not equal to
print("dog"!="cat")         #returns true
print("sumith"!="sumith")   #returns false

#less than operator
print(42<100)               #returns true
print(100<42)               #returns false

#greater than operator
print(100>42)               #returns true
print(42>100)               #returns false

#less than or equal to
my_count=100
print(my_count<=150)        #returns true
print(my_count<=90)         #returns false

#greater than or equal to
my_number=45
print(my_number>=50)        #returns false
print(my_number>=40)        #returns true
